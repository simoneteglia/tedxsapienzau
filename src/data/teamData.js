export function getLocalizedValue(value, language = "it") {
  if (typeof value === "string") {
    return value;
  }

  const isEnglish = language.toLowerCase().startsWith("en");

  return isEnglish ? (value.en ?? value.it) : (value.it ?? value.en);
}
